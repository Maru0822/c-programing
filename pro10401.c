#include <stdio.h>
int main(void)
{
    int x;
    int y;
    //足し算
    printf("%d\n",10 + 3);
    //引き算
    printf("%d\n",10 - 3);
    //掛け算
    printf("%d\n",10 * 3);
    //割り算
    printf("%d\n",10 / 3);
    //余り
    printf("%d\n",10 % 3);
    //変数を使った算術演算
    x =  2;
    printf("%d\n",10 / x);
    //式と代入文
    y = 2*3;
    printf("%d\n",y);

    return(0);
}