public class Prac0302 {
    public static void main(String[] args) {
        for(int i = 0; i < 10; i++){
            System.out.println(i+":");
            int j;
            j = i*i;
            for(int a = 0; a <= j; a++){
                System.out.printf("*");
            }
        }
    }
}
